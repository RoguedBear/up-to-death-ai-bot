def remove_from_last_till(time_till: float, keystrokes: list, images: list):
    """Removes data entries from array till the specified time from the last index"""
    for index in range(-1, -len(keystrokes), -1):
        if keystrokes[index][1] <= time_till:
            keystrokes[:] = keystrokes[:index - 1]
            images[:] = images[:index - 1]
            return


